// Markdownファイルを生成
async function generateServiceContent(service) {
  const content = serviceContents[service.id];
  if (!content) {
    console.log(`⚠️  ${service.title}のコンテンツが未定義です`);
    return;
  }

  const markdown = `---
title: ${service.title}
slug: ${service.slug}
category: ${service.category}
imagePosition: ${content.imagePosition || 'center 25%'}
---

${content.intro}

${content.problems}

${content.value}

${content.process}

${content.cases}

${content.pricing}

${content.faq}

${content.cta}
`;

  const outputPath = path.join(__dirname, '..', 'website', 'content', 'services', `${service.slug}.md`);
  await fs.writeFile(outputPath, markdown, 'utf-8');

  const charCount = markdown.length;
  console.log(`✓ ${service.title}: ${charCount}文字 (${outputPath})`);
}

// メイン処理
async function main() {
  console.log('=== サービスコンテンツ生成開始 ===\n');

  for (const service of services) {
    await generateServiceContent(service);
  }

  console.log('\n=== 生成完了 ===');
  console.log(`合計 ${services.length}ファイルを生成しました`);
}

main().catch(console.error);
